
// Common JS Module
/* Synchronous 
   File Extension optional 
   If We give full file path then we can load any file using cjs
   It is convention to add cjs in the file extension
   It is optional to set "type":"commonjs" in package.json because commonjs is the default module system in nodejs
   strict mode enabled by default
*/

// Es6 Modules
/* Asynchronous 
   File extension mandatory
   We can not load any file, only js and mjs files are allowed   
   It is a convention to add mjs is the file extension
   We must set "type":"module" in package.json
   strict mode is not enabled by default
*/

